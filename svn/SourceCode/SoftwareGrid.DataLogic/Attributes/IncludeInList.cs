using System;

namespace SoftwareGrid.DataLogic.Attributes
{
    public class IncludeInList : Attribute
    {
        public int Sequence
        {
            get;
            set;
        }

        public bool IncludeInGrid
        {
            get;
            set;
        }

        public int Width
        {
            get;
            set;
        }

        public bool AllowWidthInPercentage
        {
            get;
            set;
        }

        public bool AllowSorting
        {
            get;
            set;
        }

        public bool AllowGrouping
        {
            get;
            set;
        }

        public bool AllowFiltering
        {
            get;
            set;
        }

        public bool AllowSearch
        {
            get;
            set;
        }

        public bool IsForeignKey
        {
            get;
            set;
        }

        public string ColumnName
        {
            get;
            set;
        }

        public string DisplayName
        {
            get;
            set;
        }

        public IncludeInList()
        {
            Sequence = 0;
            Width = 100;
            AllowWidthInPercentage = false;
            AllowSorting = true;
            AllowGrouping = true;
            AllowFiltering = true;
            ColumnName = DisplayName = string.Empty;
            IncludeInGrid = true;
        }
    }
}
